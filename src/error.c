#include "so_long.h"

void	fatal(const char *msg, t_game *g)
{
	write(2, "Error\n", 6);
	if (msg)
		write(2, msg, ft_strlen(msg));
	write(2, "\n", 1);
	cleanup(g);
	exit(1);
}
